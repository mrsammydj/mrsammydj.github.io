// Create manual particle effect as a fallback or for browsers without particles.js
document.addEventListener('DOMContentLoaded', function() {
    const particlesContainer = document.getElementById('particles-js');
    
    // Skip if particles.js is working or if prefers-reduced-motion
    if (typeof particlesJS !== 'undefined' || window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
        return;
    }
    
    // Fallback: Create manual particle effect
    function createParticle() {
        const particle = document.createElement('div');
        particle.classList.add('particle');
        
        // Random position
        const x = Math.random() * 100;
        const y = Math.random() * 100;
        particle.style.left = `${x}%`;
        particle.style.top = `${y}%`;
        
        // Random size
        const size = Math.random() * 3 + 1;
        particle.style.width = `${size}px`;
        particle.style.height = `${size}px`;
        
        // Random opacity
        particle.style.opacity = Math.random() * 0.5 + 0.1;
        
        // Random animation delay
        particle.style.animationDelay = `${Math.random() * 5}s`;
        
        particlesContainer.appendChild(particle);
    }
    
    // Create multiple particles
    for (let i = 0; i < 50; i++) {
        createParticle();
    }
});